/**
 * Returns an SVG string for the named icon.
 * @param {string} name
 * @param {{ size?: number, cls?: string, strokeWidth?: number }} [opts]
 */
export function icon(name, opts = {}) {
  const raw = ICONS[name];
  if (!raw) return '';
  const size = opts.size ?? 20;
  const cls = opts.cls ? ` class="${opts.cls}"` : '';
  const sw = opts.strokeWidth;
  let out = raw.replace('<svg', `<svg width="${size}" height="${size}"${cls}`);
  if (sw) out = out.replace('stroke-width="1.8"', `stroke-width="${sw}"`);
  return out;
}

/** Hydrate all `<i data-icon="Users">` placeholders in the DOM. */
export function hydrateIcons(root = document) {
  root.querySelectorAll('[data-icon]').forEach((el) => {
    const name = el.dataset.icon;
    const size = el.dataset.iconSize ? Number(el.dataset.iconSize) : undefined;
    const cls = el.dataset.iconCls;
    el.innerHTML = icon(name, { size, cls });
  });
}
